x = int(input(""))
if x >= 90:
    print("A")
elif x <= 89 and x >= 85:
    print ("B+")
elif x <= 84 and x >= 80:
    print ("B")
elif x <= 79 and x >= 75:
    print ("C+")
elif x <= 74 and x >= 70:
    print ("C")
elif x <= 69 and x >= 65:
    print ("D+")
elif x <= 64 and x >= 60:
    print ("D")
else:
    print("F")

